    private static readonly IReadOnlyList<CultureOption> SupportedCultureOptions =
    [
        new("en", "English"),
        new("uk", "Українська"),
    ];

    /// <summary>
    /// Returns the supported UI culture options for the manual language switcher.
    /// </summary>
    public IReadOnlyList<CultureOption> SupportedCultures => SupportedCultureOptions;

    /// <summary>
    /// Returns the current UI culture code.
    /// </summary>
    public string CurrentCultureCode => NormalizeCultureCode(CultureInfo.CurrentUICulture);

    /// <summary>
    /// Resolves one UI text by key using the current request culture.
    /// </summary>
    /// <param name="key">The catalog key.</param>
    public string this[string key] => Get(key);

    /// <summary>
    /// Resolves one UI text by key using the current request culture.
    /// </summary>
    /// <param name="key">The catalog key.</param>
    public string Get(string key)
    {
        var dictionary = CurrentCultureCode == "uk" ? Ukrainian : English;
        if (dictionary.TryGetValue(key, out var value))
        {
            return value;
        }

        return English.TryGetValue(key, out var fallback) ? fallback : key;
    }

    private static string NormalizeCultureCode(CultureInfo cultureInfo)
        => cultureInfo.TwoLetterISOLanguageName.Equals("uk", StringComparison.OrdinalIgnoreCase) ? "uk" : "en";
